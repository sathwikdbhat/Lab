"""10. IPC Chatbot"""

import PyPDF2
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.metrics.pairwise import cosine_similarity

pdf=PyPDF2.PdfReader(open("ipc.pdf","rb"))

text=""
for p in pdf.pages[:20]:
    text+=p.extract_text()

sec=[i for i in text.split("Section") if len(i)>20]

while True:
    q=input("Ask (exit): ")
    if q=="exit":
        break
    vec=TfidfVectorizer()
    m=vec.fit_transform(sec+[q])
    s=cosine_similarity(m[-1],m[:-1]).flatten()
    print(sec[s.argmax()][:300])