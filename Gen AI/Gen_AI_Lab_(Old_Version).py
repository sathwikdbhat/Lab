"""3. Custom Word2Vec"""

from gensim.models import Word2Vec
import matplotlib.pyplot as plt
from sklearn.manifold import TSNE

corpus=["doctor treats patient","insulin for diabetes","heart diseases common","infection spreads fast","vaccines build immunity"]

data=[s.split() for s in corpus]

model=Word2Vec(data,size=50,min_count=1)

print("Similar to diabetes:")
for w,s in model.wv.most_similar("diabetes",topn=5):
    print(w,round(s,3))

words=list(model.wv.index2word)
vec=model.wv[words]

tsne=TSNE(n_components=2,perplexity=3).fit_transform(vec)

for i,w in enumerate(words):
    x,y=tsne[i]
    plt.scatter(x,y)
    plt.text(x,y,w)

plt.show()

