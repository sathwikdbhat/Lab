"""9. Naive Bayes (Olivetti)"""

import matplotlib.pyplot as plt
from sklearn.datasets import fetch_olivetti_faces
from sklearn.model_selection import train_test_split
from sklearn.naive_bayes import GaussianNB

data = fetch_olivetti_faces()
X,y = data.data, data.target
X_tr,X_ts,y_tr,y_ts = train_test_split(X,y,test_size=0.2)
model = GaussianNB().fit(X_tr,y_tr)
print("Accuracy:",model.score(X_ts,y_ts)*100)

for i in range(10):
  plt.subplot(2,5,i+1)
  plt.imshow(X_ts[i].reshape(64,64),cmap='gray')
  plt.title(str(y_ts[i])+" "+str(model.predict([X_ts[i]])[0]))
  plt.axis('off')
plt.show()

"""10. K-Means Clustering"""

import matplotlib.pyplot as plt
from sklearn.datasets import load_breast_cancer
from sklearn.cluster import KMeans
from sklearn.preprocessing import StandardScaler
from sklearn.decomposition import PCA

data = load_breast_cancer()
X = data.data
X = StandardScaler().fit_transform(X)
k = KMeans(n_clusters=2, random_state=42)
y = k.fit_predict(X)
p = PCA(n_components=2)
X_p = p.fit_transform(X)
c = p.transform(k.cluster_centers_)

plt.scatter(X_p[:,0],X_p[:,1],c=y)
plt.scatter(c[:,0],c[:,1],c='red', marker='X', s=100, label='Centroids')
plt.title('K-Means Clustering with PCA')
plt.xlabel('Principal Component 1')
plt.ylabel('Principal Component 2')
plt.legend()
plt.show()