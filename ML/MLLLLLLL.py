import numpy as np
import matplotlib.pyplot as plt
from collections import Counter

np.random.seed(1)
x = np.random.rand(100)
tr_x, ts_x = x[:50], x[50:]
tr_y = ["Class1" if i<=0.5 else "Class2" for i in tr_x]

for k in [1,2,3,4,5,20,30]:
  preds=[]
  for p in ts_x:
    d = abs(tr_x-p)
    idx = d.argsort()[:k]
    lab = [tr_y[i] for i in idx]
    preds.append(Counter(lab).most_common(1)[0][0])

  print(f"{k} Class1: {preds.count('Class1')} Class2: {preds.count('Class2')}")
plt.scatter(tr_x,[0]*50,c=['b' if l=='Class1' else 'r' for l in tr_y])
plt.scatter(ts_x,[1]*50,c=['b' if p=='Class1' else 'r' for p in preds], marker='x')
plt.show()

"""6. Locally Weighted Regression"""

import numpy as np
import matplotlib.pyplot as plt

X = np.linspace(0,10,50)
y = np.sin(X)
X_mat = np.c_[np.ones(50),X]
def lwr(x,X,y,t):
  w = np.exp(-((X-x)**2).sum(axis=1)/(2*t*t))
  W = np.diag(w)
  theta = np.linalg.inv(X.T.dot(W).dot(X)).dot(X.T).dot(W).dot(y)
  return x.dot(theta)

x_test = np.linspace(0,10,100)
pred = [lwr(np.array([1,i]),X_mat,y,0.5) for i in x_test]
plt.scatter(X,y, color='red')
plt.plot(x_test,pred, color='blue')
plt.show()

